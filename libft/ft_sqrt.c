#include "libft.h"

int	ft_sqrt(int nb)
{
	int cx;

	cx = 1;
	while (nb > 0 && cx <= 46340 && nb <= 2147395600)
	{
		if ((cx * cx) == nb)
			return (cx);
		cx++;
	}
	return (0);
}
